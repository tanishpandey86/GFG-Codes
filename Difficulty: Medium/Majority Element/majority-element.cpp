class Solution {
  public:
    int majorityElement(vector<int>& arr) {

        int n=arr.size();
        unordered_multiset<int> s(arr.begin(),arr.end());
        for(int i=0;i<n;i++)
        {
            if(s.count(arr[i])>n/2) return arr[i];
        }
        return -1;
    }
};

